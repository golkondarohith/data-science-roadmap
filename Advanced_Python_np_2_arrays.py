import numpy as np
#Creating arrays from lists

numpy_array = np.array([1, 2, 3, 4, 5])
print(numpy_array)
print("\n")

#creating arrays as zeros
num_zeros = np.zeros(5)
print(num_zeros)
print("\n")

#creating arrays as one's
num_ones = np.ones((2,3))
print(num_ones)
print("\n")

#creating indentity matrix
num_identity = np.eye(3)
print(num_identity)
print("\n")


