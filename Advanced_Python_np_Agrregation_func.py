import numpy as np

arr = np.array([10, 20, 30, 40, 50])

#Sum of all elements
print(np.sum(arr))
print("\n")

#Mean
print(np.mean(arr))
print("\n")

#Highest element
print(np.max(arr))
print("\n")

#Lowest element
print(np.min(arr))
print("\n")


#Standard deviation
print(np.std(arr))
print("\n")

#variance
print(np.var(arr))
print("\n")

