import numpy as np

#Finding the shape of the array
arr_2d = np.array([[1, 2, 3], [4, 5, 6]])
print(arr_2d.shape)
print("\n")


#Finding the size of the array
print(arr_2d.size)
print("\n")


#Finding the dimensions of the array
print(arr_2d.ndim)
print("\n")


#Finding the data type of the elements
print(arr_2d.dtype)
print("\n")


#Changing the datatype
print(arr_2d.astype(float))
print("\n")